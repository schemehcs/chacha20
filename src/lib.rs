pub fn chacha20_encrypt(key: &[u8; 32], ctr: u32, nonce: &[u8; 12], plaintext: &[u8]) -> Vec<u8> {
    let mut state = state_gen(key, ctr, nonce);
    let mut ciphertext = Vec::new();
    for chk in plaintext.chunks(64) {
        let key_stream = chacha20_block(&state);
        for i in 0..chk.len() {
            ciphertext.push(key_stream[i] ^ chk[i]);
        }
        state_inc_ctr(&mut state);
    }
    ciphertext
}

pub fn chacha20_decrypt(key: &[u8; 32], ctr: u32, nonce: &[u8; 12], ciphertext: &[u8]) -> Vec<u8> {
    let mut state = state_gen(key, ctr, nonce);
    let mut plaintext = Vec::new();
    for chk in ciphertext.chunks(64) {
        let key_stream = chacha20_block(&state);
        for i in 0..chk.len() {
            plaintext.push(key_stream[i] ^ chk[i]);
        }
        state_inc_ctr(&mut state);
    }
    plaintext
}

pub fn chacha20_block(initial_state: &[u32; 16]) -> [u8; 64] {
    let mut state = *initial_state;
    for _ in 0..10 {
        inner_block(&mut state);
    }
    state_add(&mut state, initial_state);
    serialize(&state)
}

pub fn state_gen(key: &[u8; 32], ctr: u32, nonce: &[u8; 12]) -> [u32; 16] {
    let mut state: [u32; 16] = [0; 16];
    // 0 - 3 constant
    state[0] = 0x61707865;
    state[1] = 0x3320646e;
    state[2] = 0x79622d32;
    state[3] = 0x6b206574;

    for (ki, chk) in (4..).zip(key.chunks_exact(4)) {
        state[ki] = u32::from_le_bytes(chk.try_into().unwrap());
    }

    // 12 ctr
    state[12] = ctr;

    // 13 - 15 nounce
    for (ni, chk) in (13..).zip(nonce.chunks_exact(4)) {
        state[ni] = u32::from_le_bytes(chk.try_into().unwrap());
    }

    state
}

fn quarter_round_pure(mut a: u32, mut b: u32, mut c: u32, mut d: u32) -> (u32, u32, u32, u32) {
    a = a.wrapping_add(b);
    d ^= a;
    d = d.rotate_left(16);
    c = c.wrapping_add(d);
    b ^= c;
    b = b.rotate_left(12);
    a = a.wrapping_add(b);
    d ^= a;
    d = d.rotate_left(8);
    c = c.wrapping_add(d);
    b ^= c;
    b = b.rotate_left(7);
    (a, b, c, d)
}

fn quarter_round(state: &mut [u32; 16], ai: usize, bi: usize, ci: usize, di: usize) {
    (state[ai], state[bi], state[ci], state[di]) =
        quarter_round_pure(state[ai], state[bi], state[ci], state[di]);
}

fn inner_block(state: &mut [u32; 16]) {
    quarter_round(state, 0, 4, 8, 12);
    quarter_round(state, 1, 5, 9, 13);
    quarter_round(state, 2, 6, 10, 14);
    quarter_round(state, 3, 7, 11, 15);
    quarter_round(state, 0, 5, 10, 15);
    quarter_round(state, 1, 6, 11, 12);
    quarter_round(state, 2, 7, 8, 13);
    quarter_round(state, 3, 4, 9, 14);
}

fn state_inc_ctr(state: &mut [u32; 16]) {
    // add block counter
    state[12] = state[12].wrapping_add(1);
}

fn state_add(a: &mut [u32; 16], b: &[u32; 16]) {
    for i in 0..16 {
        a[i] = a[i].wrapping_add(b[i])
    }
}

fn serialize(state: &[u32; 16]) -> [u8; 64] {
    let mut serialized = Vec::new();
    for u in state {
        let ule = u.to_le_bytes();
        serialized.extend_from_slice(&ule);
    }
    serialized.try_into().unwrap()
}

